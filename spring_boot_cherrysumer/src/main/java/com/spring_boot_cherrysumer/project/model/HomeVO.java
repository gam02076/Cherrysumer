package com.spring_boot_cherrysumer.project.model;

public class HomeVO {
	private int memNo;
	private String memPicture;
	private String memName;
	private String memInstargram;
	private String memFacebook;
	private String memBlog;
    public int getMemNo() {
		return memNo;
	}
	public void setMemNo(int memNo) {
		this.memNo = memNo;
	}
	public String getMemPicture() {
		return memPicture;
	}
	public void setMemPicture(String memPicture) {
		this.memPicture = memPicture;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	public String getMemInstargram() {
		return memInstargram;
	}
	public void setMemInstargram(String memInstargram) {
		this.memInstargram = memInstargram;
	}
	public String getMemFacebook() {
		return memFacebook;
	}
	public void setMemFacebook(String memFacebook) {
		this.memFacebook = memFacebook;
	}
	public String getMemBlog() {
		return memBlog;
	}
	public void setMemBlog(String memBlog) {
		this.memBlog = memBlog;
	}
}
